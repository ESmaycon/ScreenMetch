package br.com.screenmetch.modelos;

public class Serie extends Titulo {
    int numeroDeTemporadas;
    int numeroDeEpisodios;
    int minutosPorEpisodio;

    public Serie (String nome, int anoDeLancamento){
        super(nome ,anoDeLancamento);
    }

    public int getNumeroDeTemporadas() {
        return numeroDeTemporadas;
    }

    public void setNumeroDeTemporadas(int numeroDeTemporadas) {
        this.numeroDeTemporadas = numeroDeTemporadas;
    }

    public int getNumeroDeEpisodios() {
        return numeroDeEpisodios;
    }

    public void setNumeroDeEpisodios(int numeroDeEpisodios) {
        this.numeroDeEpisodios = numeroDeEpisodios;
    }

    public int getMinutosPorEpisodio() {
        return minutosPorEpisodio;
    }

    public void setMinutosPorEpisodio(int minutosPorEpisodio) {
        this.minutosPorEpisodio = minutosPorEpisodio;
    }

    @Override
    public int getDuracaoEmMinitos() {
        return numeroDeEpisodios * minutosPorEpisodio * numeroDeTemporadas;
    }

    @Override
    public String toString() {
        return "SÃ©rie: " + this.getNome() + "(" + this.getAnoDeLancamento() + ")";
    }
}
